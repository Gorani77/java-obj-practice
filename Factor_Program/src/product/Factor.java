package product;

public class Factor {
	private String name;
	private int number;
	private int price;
	private int qntty;

	public String getName() {
		return this.name;
	}
	public int getNumber() {
		return this.number;
	}
	public int getPrice() {
		return this.price;
	}
	public int getQntty() {
		return this.qntty;
	}
	public void setName(String str) {
		this.name = str;
	}
	public void setNumber(int n) {
		this.number = n;
	}
	public void setPrice(int n) {
		this.price = n;
	}
	public void setQntty(int n) {
		this.qntty = n;
	}
	
	
	
}
